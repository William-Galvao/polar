    <p>
      A primeira viagem documentada do homem branco ao extremo sul do continente americano foi realizada por Fernão de
      Magalhães em 1520, embora já tenha sido sugerido que o europeu já conhecia a região antes disso. De qualquer
      maneira, foi Magalhães quem deu o nome de Terra do Fogo à região, tendo avistado fogueiras acesas pelos índios nas
      margens do estreito que hoje leva seu nome. A bordo ia também o italiano Antonio Pigafetta, que desempenhou
      importante papel científico escrevendo o primeiro léxico da linguagem da Patagônia, elaborado com o auxílio de um
      indígena capturado. Outros navegadores depois dele se aventuraram pela Terra do Fogo, mas o primeiro que
      seguramente permaneceu um longo tempo em terra foi Alonso de Camargo, que em 1541 invernou em uma baía fueguina,
      possivelmente localizada na Ilha dos Estados ou na entrada do canal de Beagle.

      Nesta altura todo o território fora reivindicado pela coroa espanhola, mas as incursões de exploradores e
      corsários ingleses e holandeses fizeram com que a Espanha planejasse a fortificação e povoamento da região. Foram
      enviadas várias naus com oficiais e colonos mas nenhuma logrou seu intento. Enquanto isso, a geografia fueguina ia
      sendo cartografada com precisão cada vez maior. No século XVIII continuaram as viagens exploratórias,
      acrescentando-se a participação francesa, e surgiram conflitos entre as potências europeias pela soberania da
      região, registrando-se batalhas navais e diversos naufrágios, além de ser concebido o primeiro plano de
      evangelização dos indígenas, que entretanto não se concretizou. Em 1771 Manuel Pando percorreu pela primeira vez
      documentadamente o canal de Beagle e em 1788 a região foi estudada pela primeira vez por uma expedição científica,
      a mando do italiano Antonio Malaspina, que estava a serviço da Espanha. Em 1790 foi realizada outra, capitaneada
      por Juan José de Elizalde, a primeira de que participou um oficial argentino, Francisco José Cándido de Lasala.
      Aparentemente nesta época já havia se estabelecido um comércio de peles e óleo dos lobos-marinhos existentes na
      região.

      Em 22 de maio de 1826 zarpou da Inglaterra uma expedição científica liderada pelo capitão Philip Parker King. O
      capitão, contudo, por razões desconhecidas suicidou-se quando estava no estreito de Magalhães, e o comando foi
      assumido pelo jovem imediato Robert FitzRoy. A viagem teve importantes consequências: nela foram capturados alguns
      nativos fueguinos e foi explorada uma grande parte do canal de Beagle. Os nativos acabaram sendo levados para a
      Inglaterra e lá educados. Dois deles, que receberam os nomes de Jeremy Buttom e Fuegia Basket, conseguiram aceitar
      a educação e aprenderam a língua e os costumes ingleses. Sua presença na capital inglesa foi uma sensação, e
      acabaram sendo apresentados à corte. Disso resultou o projeto de uma missão para evangelizar os nativos. A viagem
      foi organizada e partiu em 27 de novembro de 1831, com FitzRoy ao comando do brigue HMS Beagle. Levava consigo o
      mais tarde celebrado naturalista Charles Darwin e o missionário Richard Matthews, bem como os índios que
      pretendiam devolver a seu povo. Enquanto o Beagle explorava os canais levantando sua cartografia e Darwin fazia
      importantes observações científicas, Matthews foi deixado em terra, em uma pequena cabana, para iniciar seu
      trabalho missionário. Com ele ficaram os índios, mas logo fugiram e retornaram ao estado selvagem. Quando o Beagle
      retornou pouco depois, Matthews estava desolado, pois além da fuga dos seus pupilos outros índios ameaçavam sua
      vida e haviam roubado tudo o que ele tinha consigo. Com isso, abandonou seu projeto e retornou para o Beagle.
