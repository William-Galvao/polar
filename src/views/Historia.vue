      projeto de estabelecimento de uma colônia penal para fomentar o povoamento. Em 1895 havia uma população de 477
      pessoas, com 225 delas vivendo na zona urbana, excluídos os índios.

      O trabalho das missões inglesas prosseguia com o beneplácito das autoridades argentinas, e atraíam indígenas de
      outras etnias, como os onas, que encontraram nelas um refúgio temporário até que as doenças também os dizimassem.
      As missões por outro lado tiveram uma importância econômica introduzindo o gado bovino, ovino e caprino na região,
      e iniciando a exploração e beneficiamento da abundante madeira da ilha. Bridges faleceu em 1898 em Buenos Aires, e
      sua obra foi continuada por seus filhos na sua Estância Harberton e outros seguidores na antiga sede na península,
      até que quase já não restavam mais índios, sendo a missão original encerrada em 1910. Os poucos índios
      remanescentes foram transferidos para outro estabelecimento na ilha Navarino, sob os cuidados do missionário John
      Williams, onde permaneceram até 1913. A constante presença dos anglicanos acabou por despertar a atenção dos
      eclesiásticos católicos argentinos. Os salesianos assumiram o encargo e enviaram para lá seu primeiro missionário
      em 1897, o padre Ramón Lista. Foi edificada uma capela em 1898, da qual foi nomeado pároco o padre Juan Fossatti.
