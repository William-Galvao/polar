    <p>
      Em 19 de junho de 1829 a Argentina, já independente, fez sua primeira afirmação de soberania sobre a Terra do
      Fogo, criando uma comandância militar, sediada nas Ilhas Malvinas. Pouco mais tarde os ingleses ocuparam as ilhas,
      dando margem a ideias de enviar novamente missionários anglicanos para a Terra do Fogo, mas as primeiras
      tentativas, organizadas por Allen Francis Gardiner, fracassaram diante da hostilidade dos indígenas e da falta de
      apoio oficial, acabando no martírio dos missionários. Enquanto isso a Argentina também fazia movimentos para a
      povoação do extremo sul. Em 1852 o oficial Luis Piedra Buena com sua família ergueu uma casa na Ilha dos Estados,
      cuja propriedade lhe foi outorgada pelo presidente Bartolomé Mitre em 1868, hasteando na ilha pela primeira vez a
      bandeira argentina. No ano seguinte Waite Hockin Stirling, antigo colaborador de Gardiner, levantou uma missão na
      ilha Navarino exatamente diante da futura Ushuaia, levando cabras e iniciando plantações a título de experiência.
      Logo compreendeu que o local não era adequado, e transladou-se para o outro lado do canal, escolhendo a península
      que fecha pela esquerda a baía de Ushuaia, onde poderia ficar mais próximo dos indígenas. Ali a partir de 18 de
      janeiro de 1869 ergueu uma casa em um ponto elevado da península, cujo sítio é hoje um monumento nacional
      argentino.

