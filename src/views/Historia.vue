      proteção dos índios, sua população decrescia dramaticamente, assolada por sucessivas epidemias trazidas pelo homem
      branco, sem contar os enfrentamentos violentos com outros exploradores europeus que resultavam em inúmeras mortes.
      Outros se entregavam à bebida ou vendiam suas filhas para se prostituírem para os marinheiros. Em meados da década
      de 1880 Bridges acusava a existência de somente 387 índios yagan em todo o arquipélago.

      Depois da partilha da Terra do Fogo entre Chile e Argentina em 1881, definindo seus limites, em 1883 o governo
      argentino decidiu enviar a "Divisão Expedicionária ao Atlântico Sul", sob o comando do coronel Augusto Lasserre, a
      fim de ali estabelecer subprefeituras. Sete navios de guerra foram designados para a missão, que aportou primeiro
      na Ilha dos Estados e em 1884 entrou na Baía de Ushuaia, apenas com quatro naus. Em 12 de outubro de 1884 foi
      enfim içada a bandeira argentina e criada a subprefeitura local, com Alejandro Virasoro y Calvo ocupando a
      primeira magistratura, auxiliado por 44 oficiais subalternos. A data constitui a fundação oficial da cidade.
      Poucos dias depois, em 16 de outubro, foi criado o Território da Terra do Fogo, do qual Ushuaia foi escolhida
      naturalmente como a capital, tendo Félix Paz como governador, e, no ano seguinte, como capital de um dos três
      Departamentos em que o Território foi subdividido. Paz transferiu em 1887 a subprefeitura para a baía Bom Sucesso,
      e logo depois para a baía Tétis, possivelmente para não confundir as administrações, mas a experiência não deu
      certo e Ushuaia voltou a acumular todas as funções em 1896.

      Enquanto os índios desapareciam quase completamente, aumentava devagar o número de colonos brancos, chegando
      grupos de espanhóis, portugueses, argentinos e italianos, bem como garimpeiros do ouro que fora descoberto no
      norte da ilha e que acabavam sempre gerando conflitos. Esses primeiros colonos brancos de Ushuaia também tiveram
      de lutar por sua própria sobrevivência, e vários abandonaram a comunidade, que sempre estava à míngua de apoio
      oficial e dependia em larga medida do intermédio da florescente cidade chilena de Punta Arenas para muitos
