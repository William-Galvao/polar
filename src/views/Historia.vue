      o verdadeiro motor do povoamento inicial e estendeu a influência missionária por toda a região, paulatinamente
      ganhando a confiança dos indígenas e reunindo-os em grande número, não sem encontrar ao mesmo tempo inúmeras
      resistências e dificuldades de toda ordem. No ano seguinte foram batizados os primeiros indígenas, realizando-se
      também os primeiros casamentos cristãos entre eles. Bridges também foi importante por ter composto o primeiro
      dicionário da língua yagan e por ter realizado a primeira tradução de um texto bíblico para este idioma, uma
